    public static String exp(String object,String... params){
        String buf = "";
        for (int i = 0; i < params.length; i++) {
            params[i] =  "'"+ params[i] +"'";
            buf += params[i];
            if(i < params.length - 1){
                buf += ",";
            }
        }
        return "#{"+object + " " + buf +"}";
    }
